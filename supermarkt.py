class Product:
    def setId(self,id):
        self.id = id
    def getId(self):
        return self.id
    def setNam(self,nam):
        self.nam = nam
    def getNam(self):
        return self.nam
    def setPrice(self,price):
        self.price = price
    def getPrice(self):
        return self.price
    def setShelfNo(self,shelfNo):
        self.shelfNo = shelfNo
    def getShelfNo(self):
        return self.shelfNo

    def setQantuty(self,qantuty):
        self.shelfNo = qantuty
    def getQantuty(self):
        return self.qantuty
